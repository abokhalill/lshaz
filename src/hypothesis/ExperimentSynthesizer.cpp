/* FL002 */
static ExperimentFile genTreatmentFalseSharing(const LatencyHypothesis &hyp) {
    /* Reproduce the *actual* finding: two hot fields co-resident on one line,
       padded to the real record footprint so allocation/layout tracks the
       source struct. 2x atomic<uint64_t> = 16B; pad out the rest. */
    unsigned sz = evidenceUnsigned(hyp, "sizeof", 64);
    std::string ty = evidenceStr(hyp, "type_name", "(unknown)");
    unsigned pad = sz > 16 ? sz - 16 : 0;

    std::ostringstream os;
    os << "// Treatment kernel: FalseSharing (FL002)\n"
       << "// Source struct: " << ty << " (" << sz << "B)\n"
       << "// a@0 and b@8 share one 64B line; a sibling core writes b.\n"
       << "#include \"common.h\"\n\n"
       << "struct Contended {\n"
       << "    std::atomic<uint64_t> a{0};\n"
       << "    std::atomic<uint64_t> b{0};\n";
    if (pad)
        os << "    char pad[" << pad << "];\n";
    os << "};\n\n"
       << "static Contended *shared;\n"
       << "static lshaz_aggressor *agg;\n\n"
       << "void treatment_setup() {\n"
       << "    shared = new Contended;\n"
       << "    agg = new lshaz_aggressor(&shared->b);  /* contends a's line */\n"
       << "}\n\n"
       << "/* stop the writer before freeing its target. */\n"
       << "void treatment_teardown() { delete agg; delete shared; }\n\n"
       << "/* RMW, not store: a relaxed store retires into the store buffer and\n"
       << "   lfence does not drain it, so the rdtsc bracket never sees the RFO\n"
       << "   stall and treatment reads identical to control. A locked fetch_add\n"
       << "   cannot retire until it owns the line — that exposes the invalidation\n"
       << "   latency this finding is about. Measured +50% p99.9 on Zen4. */\n"
       << "void treatment_kernel(uint64_t /*iteration*/) {\n"
       << "    shared->a.fetch_add(1, std::memory_order_relaxed);\n"
       << "    lshaz_do_not_optimize(shared->a);\n"
       << "}\n";
    return {"src/treatment.cpp", os.str()};
}

