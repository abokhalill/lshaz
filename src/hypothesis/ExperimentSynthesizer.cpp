ExperimentFile ExperimentSynthesizer::generateCommonHeader(
    const LatencyHypothesis &hyp) {

    std::ostringstream os;
    os << "#pragma once\n"
       << "#define _GNU_SOURCE 1  /* sched_getcpu/affinity regardless of -std */\n"
       << "// Auto-generated by lshaz-experiment-synth\n"
       << "// Hypothesis: " << hyp.hypothesisId << "\n"
       << "// Hazard class: " << hazardClassName(hyp.hazardClass) << "\n"
       << "// Evidence tier: " << evidenceTierName(hyp.evidenceTier) << "\n\n"
       << "#include <cstdint>\n"
       << "#include <atomic>\n"
       << "#include <cstring>\n"
       << "#include <cstdio>\n"
       << "#include <cstdlib>\n"
       << "#include <fstream>\n"
       << "#include <set>\n"
       << "#include <string>\n"
       << "#include <thread>\n"
       << "#include <pthread.h>\n"
       << "#include <sched.h>\n"
       << "#include <unistd.h>\n\n"
       << "/* Anti-elision: prevent dead-code elimination of measured work. */\n"
       << "inline volatile uint64_t lshaz_global_sink;\n\n"
       << "template <typename T>\n"
       << "__attribute__((noinline)) void lshaz_do_not_optimize("
       << "T const& val) {\n"
       << "    asm volatile(\"\" : : \"r,m\"(val) : \"memory\");\n"
       << "}\n\n"
       << "inline __attribute__((noinline)) void lshaz_clobber_memory() {\n"
       << "    asm volatile(\"\" : : : \"memory\");\n"
       << "}\n\n"
       << "struct LatencySample {\n"
       << "    uint64_t tsc_start;\n"
       << "    uint64_t tsc_end;\n"
       << "};\n\n"
       << "static inline uint64_t lshaz_rdtsc() {\n"
       << "    uint64_t lo, hi;\n"
       << "    asm volatile(\"lfence; rdtsc; lfence\"\n"
       << "                 : \"=a\"(lo), \"=d\"(hi));\n"
       << "    return (hi << 32) | lo;\n"
       << "}\n\n"
       << "/* Contending writer on a sibling-distinct physical core. Without a\n"
       << "   second core mutating the co-located field there is no remote RFO,\n"
       << "   hence no false sharing to measure — a single-writer 'experiment'\n"
       << "   can only ever refute the hazard it was built to test. An smt sibling\n"
       << "   shares l1d and would not ping-pong, so the core choice excludes the\n"
       << "   measured core's thread_siblings and fails loud if none remains. */\n"
       << "class lshaz_aggressor {\n"
       << "public:\n"
       << "    explicit lshaz_aggressor(std::atomic<uint64_t>* target)\n"
       << "        : target_(target) {\n"
       << "        int core = pick_core();\n"
       << "        worker_ = std::thread([this, core] {\n"
       << "            cpu_set_t set; CPU_ZERO(&set); CPU_SET(core, &set);\n"
       << "            sched_setaffinity(0, sizeof(set), &set);\n"
       << "            uint64_t v = 0;\n"
       << "            while (!stop_.load(std::memory_order_acquire))\n"
       << "                target_->store(++v, std::memory_order_relaxed);\n"
       << "        });\n"
       << "    }\n"
       << "    ~lshaz_aggressor() {\n"
       << "        stop_.store(true, std::memory_order_release);\n"
       << "        if (worker_.joinable()) worker_.join();\n"
       << "    }\n"
       << "private:\n"
       << "    static std::set<int> siblings_of(int cpu) {\n"
       << "        std::set<int> out;\n"
       << "        char path[128];\n"
       << "        std::snprintf(path, sizeof(path),\n"
       << "            \"/sys/devices/system/cpu/cpu%d/topology/thread_siblings_list\", cpu);\n"
       << "        std::ifstream f(path);\n"
       << "        std::string tok;\n"
       << "        while (std::getline(f, tok, ',')) {\n"
       << "            auto dash = tok.find('-');\n"
       << "            if (dash == std::string::npos) out.insert(std::atoi(tok.c_str()));\n"
       << "            else {\n"
       << "                int lo = std::atoi(tok.substr(0, dash).c_str());\n"
       << "                int hi = std::atoi(tok.substr(dash + 1).c_str());\n"
       << "                for (int i = lo; i <= hi; ++i) out.insert(i);\n"
       << "            }\n"
       << "        }\n"
       << "        return out;\n"
       << "    }\n"
       << "    static int pick_core() {\n"
       << "        int measured = sched_getcpu();\n"
       << "        long n = sysconf(_SC_NPROCESSORS_ONLN);\n"
       << "        std::set<int> sib = siblings_of(measured);\n"
       << "        if (sib.empty()) {\n"
       << "            /* topology unreadable (container/restricted): still run, but\n"
       << "               state plainly that smt-distinctness is unverified. */\n"
       << "            for (int c = 0; c < n; ++c) if (c != measured) {\n"
       << "                std::fprintf(stderr, \"[lshaz] WARN: smt topology unreadable; \"\n"
       << "                    \"aggressor cpu=%d may be an smt sibling of measured cpu=%d \"\n"
       << "                    \"— coherence effect may be understated.\\n\", c, measured);\n"
       << "                return c;\n"
       << "            }\n"
       << "        } else {\n"
       << "            for (int c = 0; c < n; ++c)\n"
       << "                if (c != measured && sib.find(c) == sib.end()) {\n"
       << "                    std::fprintf(stderr, \"[lshaz] false-sharing aggressor: \"\n"
       << "                        \"measured_cpu=%d aggressor_cpu=%d (physical-distinct)\\n\",\n"
       << "                        measured, c);\n"
       << "                    return c;\n"
       << "                }\n"
       << "        }\n"
       << "        std::fprintf(stderr, \"[lshaz] FATAL: no physical core distinct from \"\n"
       << "            \"cpu %d — false sharing is unreproducible on one physical core.\\n\",\n"
       << "            measured);\n"
       << "        std::abort();\n"
       << "    }\n"
       << "    std::atomic<uint64_t>* target_;\n"
       << "    std::atomic<bool> stop_{false};\n"
       << "    std::thread worker_;\n"
       << "};\n";

    return {"src/common.h", os.str()};
}

ExperimentFile ExperimentSynthesizer::generateHarness(
    const LatencyHypothesis &hyp) {

    std::ostringstream os;
    os << "// Auto-generated harness for hypothesis " << hyp.hypothesisId << "\n"
       << "#include \"common.h\"\n\n"
       << "#include <chrono>\n"
       << "#include <cstdio>\n"
       << "#include <cstdlib>\n"
       << "#include <cstring>\n"
       << "#include <pthread.h>\n"
       << "#include <sched.h>\n"
       << "#include <thread>\n"
       << "#include <vector>\n\n"
       << "/* Kernels linked from separate TUs — prevents cross-variant optimization. */\n"
       << "extern void treatment_kernel(uint64_t iteration);\n"
       << "extern void control_kernel(uint64_t iteration);\n"
       << "extern void treatment_setup();\n"
       << "extern void control_setup();\n"
       << "extern void treatment_teardown();\n"
       << "extern void control_teardown();\n\n"
       << "static void pin_thread(int core) {\n"
       << "    cpu_set_t cpuset;\n"
       << "    CPU_ZERO(&cpuset);\n"
       << "    CPU_SET(core, &cpuset);\n"
       << "    pthread_setaffinity_np(pthread_self(), sizeof(cpuset), &cpuset);\n"
       << "}\n\n"
       << "static void run_variant(\n"
       << "    void (*setup)(),\n"
       << "    void (*kernel)(uint64_t),\n"
       << "    void (*teardown)(),\n"
       << "    std::vector<LatencySample> &samples,\n"
       << "    uint64_t warmup,\n"
       << "    uint64_t count) {\n\n"
       << "    setup();\n\n"
       << "    /* Warmup: prime i-cache, branch predictors, TLB. */\n"
       << "    for (uint64_t i = 0; i < warmup; ++i) {\n"
       << "        kernel(i);\n"
       << "        lshaz_do_not_optimize(i);\n"
       << "    }\n"
       << "    lshaz_clobber_memory();\n\n"
       << "    /* Measurement: lfence-bracketed rdtsc per iteration. */\n"
       << "    samples.resize(count);\n"
       << "    for (uint64_t i = 0; i < count; ++i) {\n"
       << "        samples[i].tsc_start = lshaz_rdtsc();\n"
       << "        kernel(i);\n"
       << "        samples[i].tsc_end = lshaz_rdtsc();\n"
       << "        lshaz_do_not_optimize(i);\n"
       << "    }\n\n"
       << "    teardown();\n"
       << "}\n\n"
       << "static void write_samples(const char *path,\n"
       << "                          const std::vector<LatencySample> &samples) {\n"
       << "    FILE *f = fopen(path, \"wb\");\n"
       << "    if (!f) { perror(path); return; }\n"
       << "    fwrite(samples.data(), sizeof(LatencySample), samples.size(), f);\n"
       << "    fclose(f);\n"
       << "}\n\n"
       << "int main(int argc, char **argv) {\n"
       << "    uint64_t warmup = 10000;\n"
       << "    uint64_t count  = 100000;\n"
       << "    int core        = 4;\n"
       << "    const char *variant = \"treatment\";\n\n"
       << "    for (int i = 1; i < argc; ++i) {\n"
       << "        if (strcmp(argv[i], \"--warmup\") == 0 && i+1 < argc)\n"
       << "            warmup = strtoull(argv[++i], nullptr, 10);\n"
       << "        else if (strcmp(argv[i], \"--count\") == 0 && i+1 < argc)\n"
       << "            count = strtoull(argv[++i], nullptr, 10);\n"
       << "        else if (strcmp(argv[i], \"--core\") == 0 && i+1 < argc)\n"
       << "            core = atoi(argv[++i]);\n"
       << "        else if (strcmp(argv[i], \"--variant\") == 0 && i+1 < argc)\n"
       << "            variant = argv[++i];\n"
       << "    }\n\n"
       << "    pin_thread(core);\n"
       << "    { auto r_ = std::system(\"mkdir -p results\"); (void)r_; }\n\n"
       << "    std::vector<LatencySample> samples;\n\n"
       << "    if (strcmp(variant, \"treatment\") == 0) {\n"
       << "        run_variant(treatment_setup, treatment_kernel,\n"
       << "                    treatment_teardown, samples, warmup, count);\n"
       << "        write_samples(\"results/treatment_samples.bin\", samples);\n"
       << "    } else {\n"
       << "        run_variant(control_setup, control_kernel,\n"
       << "                    control_teardown, samples, warmup, count);\n"
       << "        write_samples(\"results/control_samples.bin\", samples);\n"
       << "    }\n\n"
       << "    return 0;\n"
       << "}\n";

    return {"src/harness.cpp", os.str()};
}

ExperimentFile ExperimentSynthesizer::generateBuildScript(
    const LatencyHypothesis &hyp) {

    std::ostringstream os;
    os << "#!/bin/bash\n"
       << "set -euo pipefail\n\n"
       << "# " << hyp.hypothesisId << "\n"
       << "# Separate TUs prevent cross-variant optimization.\n\n"
       << "CXX=${CXX:-g++}\n"
       << "FLAGS=\"-O2 -march=native -fno-lto -std=c++20 -pthread\"\n\n"
       << "mkdir -p build results\n\n"
       << "$CXX $FLAGS -c src/treatment.cpp -o build/treatment.o\n"
       << "$CXX $FLAGS -c src/control.cpp -o build/control.o\n"
       << "$CXX $FLAGS src/harness.cpp build/treatment.o build/control.o "
       << "-o experiment -Isrc\n"
       << "$CXX $FLAGS src/analyze.cpp -o analyze\n\n"
       << "echo \"[lshaz] Build complete\"\n";

    return {"scripts/build.sh", os.str()};
}

ExperimentFile ExperimentSynthesizer::generateRunAll(
    const LatencyHypothesis &hyp,
    const MeasurementPlan &plan) {

    std::ostringstream os;
    os << "#!/bin/bash\n"
       << "set -euo pipefail\n\n"
       << "# Bail early if perf_event_paranoid blocks PMU access.\n"
       << "if [ -f /proc/sys/kernel/perf_event_paranoid ]; then\n"
       << "  PARANOID=$(cat /proc/sys/kernel/perf_event_paranoid)\n"
       << "  if [ \"$PARANOID\" -gt 1 ] && [ \"$(id -u)\" -ne 0 ]; then\n"
       << "    echo \"[lshaz] ERROR: perf_event_paranoid=$PARANOID (needs <=1 or root)\"\n"
       << "    echo \"  Fix: sudo sysctl kernel.perf_event_paranoid=1\"\n"
       << "    echo \"  Or run this script with sudo.\"\n"
       << "    exit 1\n"
       << "  fi\n"
       << "fi\n\n"
       << "echo \"[lshaz] Running full experiment: "
       << plan.hypothesisId << "\"\n\n"
       << "# Pin frequency, disable turbo/THP/ASLR/C-states.\n"
       << "sudo bash scripts/setup_env.sh\n\n"
       << "# Build\n"
       << "bash scripts/build.sh\n\n"
       << "mkdir -p results\n\n"
       << "# Run treatment\n"
       << "./experiment --variant treatment\n\n"
       << "# Run control\n"
       << "./experiment --variant control\n\n"
       << "# Verdict. Guarded so a non-confirmed result still restores the machine\n"
       << "# below; the honest status is propagated at exit. Exit codes mirror\n"
       << "# ExperimentVerdict: 0 confirmed, 1 refuted, 2 error, 3 inconclusive,\n"
       << "# 4 confounded.\n"
       << "VERDICT_STATUS=0\n"
       << "./analyze results/treatment_samples.bin results/control_samples.bin "
       << "--mde " << hyp.minimumDetectableEffect
       << " --alpha " << hyp.significanceLevel << " || VERDICT_STATUS=$?\n\n"
       << "# PMU collection passes.\n";

    for (const auto &script : plan.scripts) {
        if (script.name == "setup_env.sh" || script.name == "teardown_env.sh")
            continue;
        os << "bash scripts/" << script.name << "\n";
        os << "bash scripts/" << script.name << "\n\n";
    }

    os << "# Restore.\n"
       << "sudo bash scripts/teardown_env.sh\n\n"
       << "echo \"[lshaz] Experiment complete. Results in results/\"\n"
       << "# Non-zero == not CONFIRMED above the mde (refuted/inconclusive/confounded/\n"
       << "# error). Never masked — the caller sees system truth.\n"
       << "exit $VERDICT_STATUS\n";

    return {"scripts/run_all.sh", os.str()};
}

ExperimentFile ExperimentSynthesizer::generateMakefile() {
    std::ostringstream os;
    os << "CXX ?= g++\n"
       << "CXXFLAGS = -O2 -march=native -fno-lto -std=c++20 -pthread\n\n"
       << "all: experiment analyze\n\n"
       << "build/treatment.o: src/treatment.cpp src/common.h\n"
       << "\t@mkdir -p build\n"
       << "\t$(CXX) $(CXXFLAGS) -c $< -o $@ -Isrc\n\n"
       << "build/control.o: src/control.cpp src/common.h\n"
       << "\t@mkdir -p build\n"
       << "\t$(CXX) $(CXXFLAGS) -c $< -o $@ -Isrc\n\n"
       << "experiment: src/harness.cpp build/treatment.o build/control.o\n"
       << "\t$(CXX) $(CXXFLAGS) $^ -o $@ -Isrc\n\n"
       << "analyze: src/analyze.cpp\n"
       << "\t$(CXX) $(CXXFLAGS) $< -o $@\n\n"
       << "clean:\n"
       << "\trm -rf build experiment analyze results\n\n"
       << ".PHONY: all clean\n";

    return {"Makefile", os.str()};
}

