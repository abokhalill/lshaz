/* verdict step: distribution-free tail-regression test. */
ExperimentFile ExperimentSynthesizer::generateAnalyze(
    const LatencyHypothesis &hyp) {
    std::ostringstream os;
    os << "// Auto-generated verdict tool for " << hyp.hypothesisId << "\n"
       << "#include <cstdint>\n"
       << "#include <cstdio>\n"
       << "#include <cstdlib>\n"
       << "#include <cstring>\n"
       << "#include <cmath>\n"
       << "#include <algorithm>\n"
       << "#include <random>\n"
       << "#include <vector>\n\n"
       << "namespace {\n"
       << "struct Sample { uint64_t s, e; };\n\n"
       << "// time-ordered load; non-monotonic samples (migration / tsc wrap) are\n"
       << "// dropped loudly, never silently absorbed into the distribution.\n"
       << "bool load(const char *path, std::vector<uint64_t> &d) {\n"
       << "    FILE *f = fopen(path, \"rb\");\n"
       << "    if (!f) { fprintf(stderr, \"[lshaz] cannot open %s\\n\", path); return false; }\n"
       << "    Sample s; uint64_t dropped = 0;\n"
       << "    while (fread(&s, sizeof(s), 1, f) == 1) {\n"
       << "        if (s.e < s.s) { ++dropped; continue; }\n"
       << "        d.push_back(s.e - s.s);\n"
       << "    }\n"
       << "    fclose(f);\n"
       << "    if (dropped)\n"
       << "        fprintf(stderr, \"[lshaz] %s: dropped %llu non-monotonic sample(s)\\n\",\n"
       << "                path, (unsigned long long)dropped);\n"
       << "    return true;\n"
       << "}\n\n"
       << "// nearest-rank quantile via selection; reorders v in place.\n"
       << "uint64_t q_at(std::vector<uint64_t> &v, double q) {\n"
       << "    if (v.empty()) return 0;\n"
       << "    size_t i = (size_t)(q * (double)(v.size() - 1));\n"
       << "    std::nth_element(v.begin(), v.begin() + i, v.end());\n"
       << "    return v[i];\n"
       << "}\n\n"
       << "struct CI { double lo, hi, point; };\n\n"
       << "// percentile-method bootstrap of the relative effect (qA - qB)/qB at\n"
       << "// quantile q. distribution-free: correct for the heavy-tailed, skewed\n"
       << "// latency law where a t-test's normality assumption is invalid. scratch\n"
       << "// buffers are reused across reps — no per-rep heap churn.\n"
       << "CI boot_rel(const std::vector<uint64_t> &A, const std::vector<uint64_t> &B,\n"
       << "            double q, double alpha, int reps, std::mt19937_64 &rng) {\n"
       << "    if (A.empty() || B.empty()) return {0, 0, 0};\n"
       << "    std::vector<uint64_t> ra(A.size()), rb(B.size());\n"
       << "    std::vector<double> eff; eff.reserve(reps);\n"
       << "    for (int r = 0; r < reps; ++r) {\n"
       << "        for (size_t i = 0; i < A.size(); ++i) ra[i] = A[rng() % A.size()];\n"
       << "        for (size_t i = 0; i < B.size(); ++i) rb[i] = B[rng() % B.size()];\n"
       << "        double qa = (double)q_at(ra, q), qb = (double)q_at(rb, q);\n"
       << "        eff.push_back(qb ? (qa - qb) / qb : 0.0);\n"
       << "    }\n"
       << "    std::sort(eff.begin(), eff.end());\n"
       << "    auto pick = [&](double p) {\n"
       << "        return eff[(size_t)(p * (double)(eff.size() - 1))];\n"
       << "    };\n"
       << "    std::vector<uint64_t> pa(A), pb(B);\n"
       << "    double a0 = (double)q_at(pa, q), b0 = (double)q_at(pb, q);\n"
       << "    return {pick(alpha / 2.0), pick(1.0 - alpha / 2.0),\n"
       << "            b0 ? (a0 - b0) / b0 : 0.0};\n"
       << "}\n"
       << "} // namespace\n\n"
       << "int main(int argc, char **argv) {\n"
       << "    double mde   = " << hyp.minimumDetectableEffect << ";\n"
       << "    double alpha = " << hyp.significanceLevel << ";  /* ci at 1-alpha */\n"
       << "    double power = " << hyp.power << ";     /* target 1-beta (reporting) */\n"
       << "    int    reps  = 2000;\n"
       << "    uint64_t seed = 0x9E3779B97F4A7C15ULL;  /* fixed -> reproducible verdict */\n"
       << "    const char *tp = argc > 1 ? argv[1] : \"results/treatment_samples.bin\";\n"
       << "    const char *cp = argc > 2 ? argv[2] : \"results/control_samples.bin\";\n"
       << "    for (int i = 3; i < argc; ++i) {\n"
       << "        if (!strcmp(argv[i], \"--mde\")       && i+1 < argc) mde   = atof(argv[++i]);\n"
       << "        else if (!strcmp(argv[i], \"--alpha\") && i+1 < argc) alpha = atof(argv[++i]);\n"
       << "        else if (!strcmp(argv[i], \"--bootstrap\") && i+1 < argc) reps = atoi(argv[++i]);\n"
       << "        else if (!strcmp(argv[i], \"--seed\")  && i+1 < argc) seed = strtoull(argv[++i], 0, 0);\n"
       << "    }\n\n"
       << "    std::vector<uint64_t> t, c;  /* time-ordered */\n"
       << "    if (!load(tp, t) || !load(cp, c)) return 2;\n"
       << "    if (t.empty() || c.empty()) {\n"
       << "        fprintf(stderr, \"[lshaz] FATAL: empty sample set — cannot judge.\\n\");\n"
       << "        return 2;\n"
       << "    }\n\n"
       << "    std::mt19937_64 rng(seed);\n"
       << "    const double Q = 0.999;\n\n"
       << "    /* confound guard: split control by wall-clock order. if the tail moved\n"
       << "       within the control run by as much as the effect we seek, the between-\n"
       << "       arm delta is aliased with drift and no verdict is trustworthy. */\n"
       << "    size_t h = c.size() / 2;\n"
       << "    std::vector<uint64_t> c1(c.begin(), c.begin() + h);\n"
       << "    std::vector<uint64_t> c2(c.begin() + h, c.end());\n"
       << "    CI drift = boot_rel(c2, c1, Q, alpha, reps, rng);\n"
       << "    bool drift_sig = (drift.lo > 0.0 || drift.hi < 0.0) && fabs(drift.point) >= mde;\n\n"
       << "    /* between-arm effect. */\n"
       << "    CI e = boot_rel(t, c, Q, alpha, reps, rng);\n\n"
       << "    /* point p50/p99.9 for the table (copies; boot already consumed order). */\n"
       << "    std::vector<uint64_t> ts(t), cs(c);\n"
       << "    std::sort(ts.begin(), ts.end()); std::sort(cs.begin(), cs.end());\n"
       << "    auto pctl = [](const std::vector<uint64_t> &v, double p) {\n"
       << "        return v[(size_t)(p * (double)(v.size() - 1))];\n"
       << "    };\n\n"
       << "    printf(\"samples: control=%zu treatment=%zu  (bootstrap reps=%d, alpha=%.3f)\\n\",\n"
       << "           c.size(), t.size(), reps, alpha);\n"
       << "    printf(\"cycles          control    treatment\\n\");\n"
       << "    printf(\"  p50        %10llu %10llu\\n\",\n"
       << "           (unsigned long long)pctl(cs,0.50), (unsigned long long)pctl(ts,0.50));\n"
       << "    printf(\"  p99.9      %10llu %10llu\\n\",\n"
       << "           (unsigned long long)pctl(cs,Q), (unsigned long long)pctl(ts,Q));\n"
       << "    printf(\"effect(p99.9): %+.1f%%  ci[%.3f]=[%+.1f%%, %+.1f%%]  mde=%.1f%%\\n\",\n"
       << "           e.point*100, 1-alpha, e.lo*100, e.hi*100, mde*100);\n"
       << "    printf(\"control drift: %+.1f%%  ci=[%+.1f%%, %+.1f%%]\\n\",\n"
       << "           drift.point*100, drift.lo*100, drift.hi*100);\n\n"
       << "    if (drift_sig) {\n"
       << "        printf(\"VERDICT: CONFOUNDED (control tail drifted >= mde within the run; \"\n"
       << "               \"pin freq/turbo/c-states or interleave arms)\\n\");\n"
       << "        return 4;\n"
       << "    }\n"
       << "    if (e.lo > mde) { printf(\"VERDICT: CONFIRMED\\n\"); return 0; }\n"
       << "    if (e.hi < mde) { printf(\"VERDICT: REFUTED\\n\"); return 1; }\n"
       << "    /* ci straddles mde: cannot resolve. flag under-power honestly. */\n"
       << "    if ((e.hi - e.lo) / 2.0 > mde)\n"
       << "        printf(\"  under-powered: ci half-width %.1f%% > mde %.1f%% \"\n"
       << "               \"(raise --count toward target power %.2f)\\n\",\n"
       << "               (e.hi-e.lo)/2.0*100, mde*100, power);\n"
       << "    printf(\"VERDICT: INCONCLUSIVE\\n\");\n"
       << "    return 3;\n"
       << "}\n";
    return {"src/analyze.cpp", os.str()};
}

/*
 * Per-hazard-class kernel synthesis.
 *
 * Each pair (treatment, control) isolates exactly one structural variable.
 * Treatment reproduces the hazard; control removes it. Both are compiled
 * as separate TUs and linked into a single binary to prevent the compiler
 * from optimizing across the comparison boundary.
 *
 * Invariant: every kernel defines exactly {setup, teardown, kernel}.
 * The harness calls them via function pointer — no devirtualization.
 */

namespace {

unsigned evidenceUnsigned(const LatencyHypothesis &hyp, const char *key,
                          unsigned fallback) {
    auto it = hyp.structuralEvidence.find(key);
    if (it == hyp.structuralEvidence.end()) return fallback;
    std::string val = it->second;
    if (!val.empty() && val.back() == 'B') val.pop_back();
    try { return static_cast<unsigned>(std::stoul(val)); }
    catch (...) { return fallback; }
}

std::string evidenceStr(const LatencyHypothesis &hyp, const char *key,
                        const char *fallback = "") {
    auto it = hyp.structuralEvidence.find(key);
    if (it == hyp.structuralEvidence.end()) return fallback;
    return it->second;
}

unsigned padToLine(unsigned sz) { return ((sz + 63u) / 64u) * 64u; }

} // anonymous namespace

